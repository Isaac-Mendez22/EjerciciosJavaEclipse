package ciclosProyect;

public class ForDemo {

	public static void main(String[] args) {
		int cont = 0;
		for(cont = 5; cont <= 100; cont+=5) {
			System.out.println(cont);
		}//for cont
		
		System.out.println("=====================================================================================");
		
		for(cont=100; cont>=5; cont-=5 ) {
			System.out.println(cont);
		}
	}//main

}//class fotDemo
